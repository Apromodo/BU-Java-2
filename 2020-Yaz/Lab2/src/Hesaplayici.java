
public class Hesaplayici {

	
	private static double ortalamaHesaplayici;
	
	
	public static double ortalamaBul(int yazili1, int yazili2) {
		return ortalamaHesaplayici = (yazili1 + yazili2) / 2;
	}

	public static double ortalamaBul(int yazili1, int yazili2, int yazili3) {
		return ortalamaHesaplayici = (yazili1 + yazili2 + yazili3) / 3;
	}

	public static double ortalamaBul(int yazili1, int yazili2, int yazili3, int yazili4) {
		return ortalamaHesaplayici = (yazili1 + yazili2 + yazili3 + yazili4) / 4;
	}

}
